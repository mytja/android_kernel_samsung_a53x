int apply_patch(struct regmap *regmap)
{
	const struct reg_sequence *regs, *patches;
	int num_regs, num_patches, ret;

	if (ABOX_SOC_VERSION(4, 0, 0) < CONFIG_SND_SOC_SAMSUNG_ABOX_VERSION) {
		regs = reg_default_evt1;
		num_regs = ARRAY_SIZE(reg_default_evt1);
		patches = reg_patch_evt1;
		num_patches = ARRAY_SIZE(reg_patch_evt1);
	} else {
		regs = reg_default_evt0;
		num_regs = ARRAY_SIZE(reg_default_evt0);
		patches = reg_patch_evt0;
		num_patches = ARRAY_SIZE(reg_patch_evt0);
	}

	ret = regmap_multi_reg_write(regmap, regs, num_regs);
	if (ret < 0)
		pr_err("%s: Failed to apply regmap default: %d\n", __func__, ret);

	ret = regmap_register_patch(regmap, patches, num_patches);
	if (ret < 0)
		pr_err("%s: Failed to apply regmap patch: %d\n", __func__, ret);

	return ret;
}
